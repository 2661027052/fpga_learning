`timescale 1ns / 1ps
// ============================================================================
// 测试平台: tb_led_blink
// 功能描述: 验证led_blink模块的LED翻转行为
// 测试方法: 缩短DIV_FREQ参数加速仿真，用$error自检
// ============================================================================

module tb_led_blink;

    // -----------------------------------------------------------------------
    // 测试参数：将分频系数大幅缩短以加速仿真
    // DIV_FREQ=5 表示每5个时钟周期翻转一次LED
    // -----------------------------------------------------------------------
    localparam DIV_FREQ_TEST = 5;

    // -----------------------------------------------------------------------
    // 信号声明
    // -----------------------------------------------------------------------
    reg         clk   ;  // 测试时钟
    reg         rst_n ;  // 测试复位
    wire        led   ;  // 待测LED输出

    // -----------------------------------------------------------------------
    // 待测模块实例化（参数覆写）
    // -----------------------------------------------------------------------
    led_blink #(
        .DIV_FREQ(DIV_FREQ_TEST)
    ) u_led_blink (
        .clk  (clk  ),
        .rst_n(rst_n),
        .led  (led  )
    );

    // -----------------------------------------------------------------------
    // 时钟生成：50MHz，周期20ns
    // -----------------------------------------------------------------------
    initial begin
        clk = 1'b0;
        forever #10 clk = ~clk;  // 每10ns翻转一次 → 周期20ns = 50MHz
    end

    // -----------------------------------------------------------------------
    // 测试激励：复位 → 等待 → 检查LED翻转
    // -----------------------------------------------------------------------
    initial begin
        // 生成VCD波形文件，供GTKWave或Vivado查看
        $dumpfile("tb_led_blink.vcd");
        $dumpvars(0, tb_led_blink);

        // 初始化
        rst_n = 1'b0;             // 复位有效

        // 等待20个时钟周期后释放复位
        repeat (20) @(posedge clk);
        rst_n = 1'b1;             // 释放复位
        @(posedge clk);           // 等待一个时钟，让模块进入工作状态

        // ---------------------------------------------------------------
        // 自检流程：连续观察多个DIV_FREQ周期，验证LED在每个周期边界翻转
        // 当DIV_FREQ=5时，LED应每5个时钟周期翻转一次
        // 翻转时刻：cnt == DIV_FREQ-1 (=4) 的下一个时钟周期
        // ---------------------------------------------------------------
        // 第0~4周期：LED应该为0
        // 第5~9周期：LED应该为1
        // 第10~14周期：LED应该为0
        // 以此类推...

        led_check(0, 5, 1'b0);    // 第0~4周期，LED应为0
        led_check(5, 5, 1'b1);    // 第5~9周期，LED应为1
        led_check(10, 5, 1'b0);   // 第10~14周期，LED应为0
        led_check(15, 5, 1'b1);   // 第15~19周期，LED应为1
        led_check(20, 5, 1'b0);   // 第20~24周期，LED应为0

        // 额外检查：再验证两个完整周期以确保稳定性
        led_check(25, 5, 1'b1);   // 第25~29周期，LED应为1
        led_check(30, 5, 1'b0);   // 第30~34周期，LED应为0

        // 仿真结束
        $display("========================================");
        $display("  led_blink 测试完成，所有检查通过！");
        $display("========================================");
        #100;
        $finish;
    end

    // -----------------------------------------------------------------------
    // 自检任务：在指定范围内检查LED状态
    // start_cycle: 起始时钟周期
    // num_cycles:  检查的时钟周期数
    // exp_led:     期望的LED值
    // -----------------------------------------------------------------------
    task led_check;
        input integer start_cycle;
        input integer num_cycles;
        input        exp_led;
        integer i;
        begin
            for (i = 0; i < num_cycles; i = i + 1) begin
                @(posedge clk);
                #1;  // 等待组合逻辑稳定（避免与时钟沿竞争）
                if (led !== exp_led) begin
                    $error("[FAIL] 周期 %0d: LED=%b, 期望=%b",
                           start_cycle + i, led, exp_led);
                end else begin
                    $display("[PASS] 周期 %0d: LED=%b (正确)",
                           start_cycle + i, led);
                end
            end
        end
    endtask

    // -----------------------------------------------------------------------
    // 超时保护：防止仿真无限运行
    // -----------------------------------------------------------------------
    initial begin
        #5000;
        $display("========================================");
        $display("  仿真超时，请检查模块逻辑");
        $display("========================================");
        $finish;
    end

endmodule

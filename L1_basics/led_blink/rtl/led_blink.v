`timescale 1ns / 1ps
// ============================================================================
// 模块名称: led_blink
// 功能描述: 通过计数器分频实现LED闪烁（亮0.5s灭0.5s）
// 知识点: Verilog基础语法 - 计数器分频 + LED输出
// 时钟: 50MHz → 周期20ns
// 参数: DIV_FREQ 控制闪烁频率，默认25_000_000 → 0.5s翻转一次
// ============================================================================

module led_blink #(
    // 分频系数：时钟频率50MHz，LED每0.5s翻转一次
    // 计算公式: DIV_FREQ = 时钟频率(Hz) × 半周期时间(s) = 50_000_000 × 0.5 = 25_000_000
    parameter DIV_FREQ = 25_000_000
)(
    input  wire       clk   ,  // 系统时钟，50MHz
    input  wire       rst_n ,  // 全局复位，低电平有效（异步复位）
    output reg        led      // LED输出：1-亮 0-灭（假设高电平有效）
);

    // -----------------------------------------------------------------------
    // 计数器：从0计数到DIV_FREQ-1
    // 位宽：log2(25_000_000) ≈ 24.6，取32位留有余量
    // -----------------------------------------------------------------------
    reg [31:0] cnt;

    // 计数器逻辑：每个时钟上升沿计数，复位归零，达到上限后归零
    always @(posedge clk or negedge rst_n) begin
        if (~rst_n) begin
            cnt <= 32'd0;                     // 复位时计数器清零
        end else if (cnt == DIV_FREQ - 1) begin
            cnt <= 32'd0;                     // 计数到上限，回零
        end else begin
            cnt <= cnt + 1'b1;                // 正常递增计数
        end
    end

    // -----------------------------------------------------------------------
    // LED输出控制：计数器每满一个周期，LED状态翻转一次
    // 实现 0.5s亮 → 0.5s灭 → 0.5s亮 → ... 的闪烁效果
    // -----------------------------------------------------------------------
    always @(posedge clk or negedge rst_n) begin
        if (~rst_n) begin
            led <= 1'b0;                      // 复位时LED熄灭
        end else if (cnt == DIV_FREQ - 1) begin
            led <= ~led;                      // 计数到上限时翻转LED状态
        end
    end

endmodule
